  static String translateSets(String setsText, String langCode) {
    if (langCode == 'tr') return setsText;
    
    String translated = setsText;
    translated = translated.replaceAll('Set', translate('set_label', langCode));
    translated = translated.replaceAll('Tekrar', translate('rep_label', langCode));
    translated = translated.replaceAll('Saniye', translate('seconds', langCode));
    translated = translated.replaceAll('sn', translate('seconds', langCode));
    translated = translated.replaceAll('toplam', 'total');
    translated = translated.replaceAll('sağ + sol', 'right + left');
    translated = translated.replaceAll('her iki taraf', 'both sides');
    
    return translated;
  }
